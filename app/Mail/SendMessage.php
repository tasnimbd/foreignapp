<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class SendMessage extends Mailable
{
    use Queueable, SerializesModels;

    /**
     * Create a new message instance.
     *
     * @return void
     */

    public $data;

    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $data = $this->data;
		
		return $this->from('mail@foreigncompanyregistration.com', 'Customer Feedback from  Foreigncompanyregistration.com')
                    ->view('sendmessage', compact('data'))
                    ->subject('Customer Feedback from  Foreigncompanyregistration.com');
    }
}
